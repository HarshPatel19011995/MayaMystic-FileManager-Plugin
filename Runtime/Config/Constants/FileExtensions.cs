namespace MayaMystic.FileManager.Config.Constants
{
	/// <summary>
	/// Contains supported file extension constants used by the MayaMystic File Manager system.
	/// </summary>
	public static class FileExtensions
	{
		/// <summary>
		/// JSON file extension.
		/// </summary>
		public const string JSON = "json";

		/// <summary>
		/// Plain text file extension.
		/// </summary>
		public const string TEXT = "txt";

		/// <summary>
		/// Binary file extension.
		/// </summary>
		public const string BINARY = "bytes";

		/// <summary>
		/// PNG image file extension.
		/// </summary>
		public const string PNG = "png";

		/// <summary>
		/// JPG image file extension.
		/// </summary>
		public const string JPG = "jpg";

		/// <summary>
		/// MP3 audio file extension.
		/// </summary>
		public const string MP3 = "mp3";

		/// <summary>
		/// MP4 video file extension.
		/// </summary>
		public const string MP4 = "mp4";

		/// <summary>
		/// ZIP archive file extension.
		/// </summary>
		public const string ZIP = "zip";
	}
}